  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    Connection cn = null;
    try {
      // Datos
      String cuenta = txtCuenta.getText();
      double importe = Double.parseDouble(txtImporte.getText());
      // Obtener conexiÃ³n
      cn = AccesoDB.getConnection();
      //cn.setTransactionIsolation(Connection.TRANSACTION_SERIALIZABLE);
      // Inicio de Tx.
      cn.setAutoCommit(false);
      // Leer saldo
      double saldo;
      int contMov;
      String sql = "select dec_cuensaldo, int_cuencontmov "
              + "from cuenta "
              + "where chr_cuencodigo=? for update";
      PreparedStatement ps = cn.prepareStatement(sql);
      ps.setString(1, cuenta);
      ResultSet rs = ps.executeQuery();
      if(!rs.next()){
        throw new Exception("ERROR: cuenta no existe.");
      }
      saldo = rs.getDouble("dec_cuensaldo");
      contMov = rs.getInt("int_cuencontmov");
      rs.close();
      ps.close();
      Thread.currentThread().sleep(1000);
      // Verificar saldo
      if(importe > saldo){
        throw new Exception("ERROR: saldo es insuficiente.");
      }
      // Actualizar saldo
      saldo -= importe;
      contMov++;
      sql = "update cuenta set dec_cuensaldo=?, int_cuencontmov=? "
              + "where chr_cuencodigo=?";
      ps = cn.prepareStatement(sql);
      ps.setDouble(1, saldo);
      ps.setInt(2, contMov);
      ps.setString(3, cuenta);
      ps.executeUpdate();
      ps.close();
      // Registrar movimiento
      sql ="insert into movimiento(chr_cuencodigo, int_movinumero, "
              + "dtt_movifecha, chr_emplcodigo, chr_tipocodigo, "
              + "dec_moviimporte) values(?,?,SYSDATE,'0002','004',?)";
      ps = cn.prepareStatement(sql);
      ps.setString(1, cuenta);
      ps.setInt(2, contMov);
      ps.setDouble(3, importe);
      ps.executeUpdate();
      ps.close();
      // Confirmar Tx
      cn.commit();
      JOptionPane.showMessageDialog(rootPane, "Proceso ok.");
    } catch (Exception e) {
      try {
        if(cn != null) cn.rollback();
      } catch (Exception e1) {
      }
      JOptionPane.showMessageDialog(rootPane, e.getMessage());
    } finally{
      try {
        if(cn != null) cn.close();
      } catch (Exception e) {
      }
    }
  }//GEN-LAST:event_btnProcesarActionPerformed

