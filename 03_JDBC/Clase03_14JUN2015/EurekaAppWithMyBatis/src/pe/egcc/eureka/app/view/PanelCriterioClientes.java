  private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    try {
      // Preparar parametro
      ClienteBean bean = new ClienteBean();
      bean.setCodigo(txtCodigo.getText());
      bean.setPaterno(txtPaterno.getText());
      bean.setMaterno(txtMaterno.getText());
      bean.setNombre(txtNombre.getText());
      bean.setEmail(txtEmail.getText());
      bean.establecerDatos();
      // Proceso
      ClienteController control = new ClienteController();
      List<ClienteBean> lista = control.consultarClientes(bean);
      // Cargar datos en la tabla
      DefaultTableModel tabla;
      tabla = (DefaultTableModel) tablaDatos.getModel();
      tabla.setRowCount(0);
      for (ClienteBean o : lista) {
        Object[] rowData = {o.getCodigo(), o.getPaterno(),
        o.getMaterno(), o.getNombre(), o.getCiudad(),
        o.getDireccion(), o.getTelefono(), o.getEmail()};
        tabla.addRow(rowData);
      }
    } catch (Exception e) {
      MyMessage.showError(this, e.getMessage());
    }
  }//GEN-LAST:event_btnBuscarActionPerformed


